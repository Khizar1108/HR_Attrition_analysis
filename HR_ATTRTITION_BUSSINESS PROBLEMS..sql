## JOB ROLE ANALYSIS .
# WHICH JOB ROLE HAS HIGH ATTRITION..?

SELECT 
    JobRole,
    COUNT(*) AS total_employees,
    SUM(CASE WHEN Attrition='Yes' THEN 1 ELSE 0 END) AS attrition_count
FROM employees
GROUP BY JobRole
ORDER BY attrition_count DESC;

# INSIGHTS: IN LABORATORY TECHNICITION,SALES EXECUTIVE,RESERCH SCIENTIST HAS HIGH ATTRITION RATE.

# EXPERIENCE ANALYSIS :
## DO NEW EMPLOYEES LEAVES MORE OR EXPIRENCE ..?

SELECT 
    YearsAtCompany,
    COUNT(*) AS total_employees,
    SUM(CASE WHEN Attrition='Yes' THEN 1 ELSE 0 END) AS attrition_count
FROM employees
GROUP BY YearsAtCompany
ORDER BY YearsAtCompany ;

## INSIGHTS 1-2 YEARS OF EXPERINCE HAS HIGH ATTRITION RATE.

